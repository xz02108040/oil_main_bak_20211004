    <tr>
        <td height="64" colspan="2" style="text-align: center; font-size: 16pt;"><strong>高空工作車作業環境危害因素及安全衛生告知單(帶入危害告知內容)</strong></td>
    </tr>

    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align: text-top;">一、</td>
        <td height="20" style="text-align: left; font-size: 12pt;">高空工作車應經檢查合格方得供他人使用。（雇主或代理人應於車上貼檢查合格標誌）</td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">二、</td>
        <td height="20" style="text-align: left; font-size: 12pt;">承攬商應於事前依作業場所之狀況、高空工作車之種類、容量等訂定包括作業方法之作業
            計畫，使作業勞工周知，並指定專人指揮監督勞工依計畫從事作業。</td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">三、</td>
        <td height="20" style="text-align: left; font-size: 12pt;">防止高空工作車之翻倒或翻落，危害勞工，應將其外伸撐座完全伸出，並採取防止地盤不
            均勻沉陷、路肩之崩塌等必要措施。</td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">四、
        </td>
        <td height="20">操作者與工作台上之勞工間之連絡正確，應規定統一指揮信號，並指定專人依該信號從事
            指揮作業等必要措施，操作人員應經教育訓練合格，才可操作高空工作車。
        </td>
    </tr>
    <tr>
        <td width="48" height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">五、</td>
        <td width="642">不得搭載勞工。(但乘坐席位及工作台，不在此限。)</td>
    </tr>
    <tr>
        <td height="20">六、</td>
        <td height="20">不得超過高空工作車之積載荷重及能力。</td>
    </tr>
    <tr>
        <td height="20">七、</td>
        <td height="20">不得使高空工作車供為主要用途以外之用途（例如起重吊掛作業）。</td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">八、</td>
        <td height="20">使用高空工作車從事作業時，雇主應使該高空工作車工作台上之勞工佩帶及確實勾掛安全帶。</td>
    </tr>
    <tr>
        <td height="20">九、</td>
        <td height="20">高空工作車之駕駛於離開駕駛座時，駕駛採取下列措施：</td>
    </tr>
    <tr>
        <td height="20">&nbsp;</td>
        <td height="20">1、將工作台下降至最低位置。</td>
    </tr>
    <tr>
        <td height="20">&nbsp;</td>
        <td height="20">2、採取預防高空工作車逸走之措施，如停止原動機並確實使用制動裝置制動等，以保持於
            穩定狀態。
        </td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">十、</td>
        <td height="20">勞工在工作台從事作業或將從事作業時，前條駕駛離開駕駛座，雇主應使駕駛確實使用制
            動裝置制動等，以保持高空工作車於穩定狀態。</td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">十一、</td>
        <td height="20">採自行行駛或以牽引拖曳將之裝卸於貨車等方式，運送高空工作車時，如使用道板或利
            用填土等方式裝卸於車輛，為防止該高空工作車之翻倒或翻落等危害，應採取下列措施：
        </td>
    </tr>
    <tr>
        <td height="20">&nbsp;</td>
        <td height="20">1、裝卸時選擇於平坦堅固地點。</td>
    </tr>
    <tr>
        <td height="20">&nbsp;</td>
        <td height="20">2、使用道板時，應使用具有足夠長度、寬度及強度之道板，且應穩固固定該道板於適當
            之斜度。
        </td>
    </tr>
    <tr>
        <td height="20">&nbsp;</td>
        <td height="20">3、使用填土或臨時架台時，應確認具有足夠寬度、強度，並保持適當之斜度。</td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">十二、</td>
        <td height="20">從事高空工作車之修理、工作台之裝設或拆卸作業時，應指定專人監督該項作業，並執行下列事項：
        </td>
    </tr>
    <tr>
        <td height="20">&nbsp;</td>
        <td height="20">1、決定作業步驟並指揮作業。</td>
    </tr>
    <tr>
        <td height="20">&nbsp;</td>
        <td height="20">2、監視作業中安全支柱、安全塊之使用狀況。
        </td>
	<tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">十三、</td>
        <td height="20">勞工於高空工作車升起之伸臂等下方從事修理、檢點等作業時，應使從事該作業勞工使用安全支柱、安全塊等，以防止伸臂等之意外落下致危害勞工。
        </td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">十四、</td>
        <td height="20">高空工作車行駛時，除有工作台可操作行駛構造之高空工作車外，不得使勞工搭載於該
            高空工作車之工作台上。但使該高空工作車行駛於平坦堅固之場所，並採取下列措施時，
            不在此限：</td>
    </tr>
    <tr>
        <td height="20"></td>
        <td height="20">1、規定一定之信號，並指定引導人員，依該信號引導高空工作車。</td>
    </tr>
    <tr>
        <td height="20"></td>
        <td height="20">2、於作業前，事先視作業時該高空工作車工作台之高度及伸臂長度等，規定適當之速率，
            並使駕駛人員依該規定速率行駛。
        </td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">十五、</td>
        <td height="20">高空工作車有工作台可操作行駛之構造者，於平坦堅固之場所以外之場所行駛時，應採
            取下列措施：</td>
    </tr>
    <tr>
        <td height="20"></td>
        <td height="20">1、規定一定之信號，並指定引導人員，依該信號引導高空工作車。</td>
    </tr>
    <tr>
        <td height="20"></td>
        <td height="20">2、於作業前，事先視作業時該高空工作車工作台之高度及伸臂長度、作業場所之地形及地
            盤之狀態等，規定適當之速率，並使駕駛人員依該規定速率行駛。
        </td>
    </tr>
    <tr>
        <td height="20">十六、</td>
        <td height="20">高空工作車之構造，應符合國家標準一四九六五規定；若有其他法令規定，應一併遵守。</td>
    </tr>
    <tr>
        <td height="20" style="text-align: left; font-size: 12pt;vertical-align:text-top;">十七、</td>
        <td height="20">高空工作車非施工必要時，不得緊靠或扣住於管架及設備；施工時應注意工作台若緊靠或扣住於管架及設備，會產生工作台因作用力反彈之風險，導致人員受傷之危害。</td>
    </tr>
    <tr>
        <td height="20">十八、</td>
        <td height="20">高空工作車應於每日作業前實施檢點及每月、每年實施定期檢查，並備紀錄供查核。</td>
    </tr>
